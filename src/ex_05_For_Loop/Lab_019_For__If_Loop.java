package ex_05_For_Loop;

public class Lab_019_For__If_Loop {

    public static void main(String[] args) {

        for (int reeja = 1; reeja < 18; reeja++) {
            if (reeja < 15) {
                System.out.println("No gift");
            } else {
                System.out.println("Iphone");
            }
        }
    }
}